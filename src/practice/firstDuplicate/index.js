function firstDuplicate(a) {
    let idx;

    for(let i = 0; i<a.length; i++){
        for(let j = 0; j<a.length; j++){
            if(i !== j && (a[i] === a[j]) && i < j) {
                if(!idx) {
                    idx = j;
                } else if (j<idx){
                    idx = j;
                } 
            }
        }
    }

    if (!idx) return -1;
    
    return a[idx];
}

